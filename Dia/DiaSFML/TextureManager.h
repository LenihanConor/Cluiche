#pragma once

#include <unordered_map>
#include <string>
#include <mutex>

namespace sf
{
	class Texture;
}

namespace Dia
{
	namespace SFML
	{
		////////////////////////////////////////////////////////////
		/// \brief Manages texture loading and caching
		///
		/// Thread-safe texture manager that loads textures from disk,
		/// caches them, and provides access via handle IDs.
		////////////////////////////////////////////////////////////
		class TextureManager
		{
		public:
			////////////////////////////////////////////////////////////
			/// \brief Constructor
			////////////////////////////////////////////////////////////
			TextureManager();

			////////////////////////////////////////////////////////////
			/// \brief Destructor - frees all loaded textures
			////////////////////////////////////////////////////////////
			~TextureManager();

			////////////////////////////////////////////////////////////
			/// \brief Load a texture and return its handle ID
			///
			/// If the texture is already loaded, returns existing ID.
			/// Thread-safe.
			///
			/// \param path File path to texture
			/// \return Texture handle ID (0 = invalid/failed to load)
			////////////////////////////////////////////////////////////
			unsigned int LoadTexture(const char* path);

			////////////////////////////////////////////////////////////
			/// \brief Get texture by handle ID
			///
			/// Thread-safe. Returns nullptr if ID is invalid.
			///
			/// \param textureId Texture handle ID
			/// \return Pointer to SFML texture, or nullptr
			////////////////////////////////////////////////////////////
			const sf::Texture* GetTexture(unsigned int textureId) const;

			////////////////////////////////////////////////////////////
			/// \brief Unload a specific texture
			///
			/// \param textureId Texture handle ID to unload
			////////////////////////////////////////////////////////////
			void UnloadTexture(unsigned int textureId);

			////////////////////////////////////////////////////////////
			/// \brief Unload all textures
			////////////////////////////////////////////////////////////
			void UnloadAll();

			////////////////////////////////////////////////////////////
			/// \brief Get number of loaded textures
			////////////////////////////////////////////////////////////
			unsigned int GetLoadedCount() const;

		private:
			////////////////////////////////////////////////////////////
			// Member data
			////////////////////////////////////////////////////////////
			mutable std::mutex mMutex; ///< Thread safety

			// Map: file path -> texture ID
			std::unordered_map<std::string, unsigned int> mPathToId;

			// Map: texture ID -> SFML texture
			std::unordered_map<unsigned int, sf::Texture*> mIdToTexture;

			unsigned int mNextId; ///< Next available texture ID (starts at 1, 0 = invalid)
		};
	}
}
