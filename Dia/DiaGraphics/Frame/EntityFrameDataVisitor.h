#pragma once

namespace Dia
{
	namespace Graphics
	{
		class EntityFrameData;

		////////////////////////////////////////////////////////////
		/// \brief Visitor interface for processing EntityFrameData
		///
		/// Implement this interface to traverse and render entity
		/// frame data. Backend implementations (SFML, DirectX, etc.)
		/// provide concrete visitors.
		////////////////////////////////////////////////////////////
		class EntityFrameDataVisitor
		{
		public:
			////////////////////////////////////////////////////////////
			/// \brief Virtual destructor
			////////////////////////////////////////////////////////////
			virtual ~EntityFrameDataVisitor() {}

			////////////////////////////////////////////////////////////
			/// \brief Visit and process entity frame data
			///
			/// \param data Frame data containing sprite draw commands
			////////////////////////////////////////////////////////////
			virtual void Visit(const EntityFrameData& data) const = 0;
		};
	}
}
